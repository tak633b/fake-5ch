// The browser chrome: the fake browser window (address bar, nav buttons) that
// wraps every view. Content pages live inside a sandboxed iframe so the model's
// CSS can never touch the chrome. SERPs and the home page are our own markup.
import { getSettings, type Settings } from './settings';

const esc = (s: string) =>
  String(s).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');

interface Boot { mode: 'home' | 'serp' | 'page' | 'settings'; cached: boolean; q?: string; url?: string; from?: string; ctx?: string; }

function shell(opts: { title: string; address: string; content: string; boot: Boot }): string {
  const bootJson = JSON.stringify(opts.boot).replace(/</g, '\\u003c');
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<title>${esc(opts.title)}</title>
<link rel="stylesheet" href="/public/chrome.css">
</head>
<body>
<header id="chrome">
  <div class="nav">
    <button class="navbtn" id="back" title="Back">‹</button>
    <button class="navbtn" id="fwd" title="Forward">›</button>
    <button class="navbtn" id="reload" title="Reload">⟳</button>
    <button class="navbtn" id="home" title="Home">⌂</button>
  </div>
  <form id="omnibox" autocomplete="off">
    <span class="lock">🔒</span>
    <input id="address" name="q" value="${esc(opts.address)}" placeholder="スレタイ検索 or 5ch URL を入力…" spellcheck="false">
  </form>
  <a class="navbtn gear" href="/settings" title="設定 — モデル & エンドポイント">⚙</a>
  <div class="brand">🍣 fake&#8209;5ch <span class="brand-model" title="every byte generated by this model">${esc(getSettings().model)}</span></div>
</header>
<main id="viewport">
${opts.content}
</main>
<script>window.__BOOT=${bootJson};</script>
<script src="/public/app.js"></script>
</body>
</html>`;
}

