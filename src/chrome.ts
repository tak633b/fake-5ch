export function renderSerp(opts: { query: string; cachedHtml?: string }): string {
  const inner = opts.cachedHtml ?? skeletonSerp();
  const content = `<div id="serp-root" class="serp-wrap">${inner}</div>${overlay()}`;
  return shell({
    title: `${opts.query} — LLMnet Search`,
    address: opts.query,
    content,
    boot: { mode: 'serp', cached: !!opts.cachedHtml, q: opts.query },
  });
}

export function renderPage(opts: { fakeUrl: string; from?: string; ctx?: string; cached: boolean }): string {
  const iframe = opts.cached
    ? `<iframe id="page-frame" sandbox="allow-same-origin allow-top-navigation-by-user-activation allow-popups allow-forms" src="/raw?url=${encodeURIComponent(opts.fakeUrl)}"></iframe>`
    : `<iframe id="page-frame" sandbox="allow-same-origin allow-top-navigation-by-user-activation allow-popups allow-forms"></iframe>`;
  const content = `${iframe}${overlay()}`;
  return shell({
    title: opts.fakeUrl,
    address: opts.fakeUrl,
    content,
    boot: { mode: 'page', cached: opts.cached, url: opts.fakeUrl, from: opts.from, ctx: opts.ctx },
  });
}

export function renderErrorBody(message: string, links: string[] = []): string {
  // A styled, in-universe error page that STILL contains working links so the
  // user can keep browsing instead of hitting a dead end.
  const linkHtml = links.map((l) => `<li><a href="/view?url=${encodeURIComponent(l)}" target="_top">${esc(l)}</a></li>`).join('');
  return `<!DOCTYPE html><html><head><meta charset="utf-8"><style>
    body{font-family:system-ui,sans-serif;background:#fff;color:#202124;display:flex;min-height:100vh;align-items:center;justify-content:center;margin:0}
    .box{max-width:560px;padding:40px;text-align:center}
    h1{font-size:42px;margin:0}.sub{color:#5f6368;margin:12px 0 24px}
    code{background:#f1f3f4;padding:2px 6px;border-radius:4px}
    ul{text-align:left;display:inline-block}
  </style></head><body><div class="box">
    <h1>This page failed to materialize</h1>
    <p class="sub">${esc(message)}</p>
    ${links.length ? `<p>But these places exist:</p><ul>${linkHtml}</ul>` : ''}
  </div></body></html>`;
}

